def character_to_value(character):
    shift = 63
    value = 0
    assert(len(character) == 64)
    # Transpose rows/columns and bits to characters are oriented corrected on the OLED
    trans = []
    for i in range(8):
        for n in range(8):
            trans.append(character[8*(7-n) + (7-i)])

    for bit in trans:
        value += (bit << shift)
        shift -= 1
    return value

